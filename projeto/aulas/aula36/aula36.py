"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""
contador = 10
print(contador)

print('-' * 15)

contador = 10
contador += 1
print(contador)

print('-' * 15)

contador = 10
contador += 2
print(contador)

print('-' * 15)

contador = 10
contador -= 2
print(contador)

print('-' * 15)

contador = 10
contador *= 2
print(contador)

print('-' * 15)

contador = 10
contador *= '2'
print(contador)

print('-' * 15)

contador = 10
contador /= 2
print(contador)

print('-' * 15)

contador = 10
contador //= 2
print(contador)

print('-' * 15)

contador = 10
contador **= 2
print(contador)

print('-' * 15)

contador = 10
contador %= 2
print(contador)
