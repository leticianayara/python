"""
Python = Linguagem de Programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

#Strings
#Aspas Simples
print('Luiz Otávio')
print('Luiz "Otávio"')

#Aspas duplas
print("Luiz Otávio")
print("Luiz 'Otávio'")

#Escape
print("Luiz \"Otávio\"")

#r - expressoes regulares
print(r"Luiz \"Otávio\"")