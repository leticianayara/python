# Tipos int e float

# int -> Números inteiros
# O tipo int representa qualquer número 
# positivo ou negativo. int sem sinal é considerado
# positivo
print(11)
print(-11)
print(0)

# float -> número com ponto flutuante
# O tipo do float reresenta qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é consitraro positivo
print(11.0)
print(-11.0)
print(0.0)

# A funcão type mostra o tipo que o Python
# inferiu ao  valor
print(type(11))
print(type(11.0),type(1.1),type(0.0) )
print(type('11.0'))